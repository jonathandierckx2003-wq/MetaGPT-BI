SELECT DISTINCT
    "Uniqe Id" AS product_id,
    "Product Name" AS product_name,
    "Category" AS product_category,
    "Brand Name" AS brand_name,
    "Asin" AS asin,
    "Upc Ean Code" AS upc_ean_code,
    CAST("List Price" AS DOUBLE) AS list_price,
    CAST("Selling Price" AS DOUBLE) AS selling_price,
    CAST("Quantity" AS INTEGER) AS quantity,
    "Model Number" AS model_number,
    "About Product" AS about_product,
    "Product Specification" AS product_specification,
    "Technical Details" AS technical_details,
    CAST("Shipping Weight" AS DOUBLE) AS shipping_weight,
    "Product Dimensions" AS product_dimensions,
    "Image" AS image,
    "Variants" AS variants,
    "Sku" AS sku,
    "Product Url" AS product_url,
    "Stock" AS stock,
    "Product Details" AS product_details,
    "Dimensions" AS dimensions,
    "Color" AS color,
    "Ingredients" AS ingredients,
    "Direction To Use" AS direction_to_use,
    "Is Amazon Seller" AS is_amazon_seller,
    "Size Quantity Variant" AS size_quantity_variant,
    "Product Description" AS product_description
FROM stg_product_details